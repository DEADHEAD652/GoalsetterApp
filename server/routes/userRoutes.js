const express = require("express");
const route = express.Router();
const User = require("../models/userModal");
//register user
route.post("/", async (req, res) => {
  const user = new User(req.body);
  try {
    await user.save();
    res.status(201).send(user);
  } catch (e) {
    res.status(400).send(e);
  }
});

